print('isalpha return true if contain only letter and not blank')
print('khanhnguyendang'.isalpha())
print('Nguyen Dang Khanh'.isalpha())

print('isalnum return true if contain only letter and number and not blank')
print('khanhnd185'.isalnum())
print('Khanh was born in 1996'.isalnum())

print('isdecimal return true if contain only numberic characters and not blank')
print('123456'.isdecimal())
print('23.123456'.isdecimal())

print('isspace return true if contain only tab, spaces, newline and not blank')
print('   \t \n '.isspace())
print(''.isspace())

print('istitle return true if contain only words that begin with an uppercase letter followed by only lowercase letters')
print('Nguyen Dang Khanh'.istitle())
print('NguyenDangKhanh'.istitle())
print('Nguyendangkhanh'.istitle())

